					<p>Skies Fall Media Group was founded in 2008 by Skillet (Atlantic Records) guitarist Ben Kasica with artist and entrepreneur Joe Snyder in order to facilitate a wide range of creative business development in the realm of the arts and media. The company was birthed from a vision to bring professional assistance to talented working artists, musicians and media developers, providing a relevant and profitable context in which to express their crafts - from music and photography to film and video to fine art and design. We strive to identify, cultivate and promote the gifting and creativity of our team and our clients in order to assist them in being as productive, creative, and successful as possible.  We endeavor to assist our clients in presenting to the public words, sights and sounds poised to entertain, instruct, arouse and inspire.</p>
